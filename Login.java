    private String validateForm(){
        List<String> flag = new ArrayList<String>();
        String alert = "";

        String user = tf_user.getText();
        
        if(user.length() < 1){
            flag.add("Username cannot be blank!");
        }

        String pass = pf_pass.getText();
        
        if(pass.length() < 1){
            flag.add("Password cannot be blank!");
        }

        if (flag.size() > 0) {
            for (String msg : flag) {
                alert += (msg + "\n");
            }
        }
        return alert;

    }
